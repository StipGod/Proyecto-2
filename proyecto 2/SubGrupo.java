public class SubGrupo
{
  private double capitalRecibido;
  private double importancia;
  
  public void setCapitalRecibido(double cR)
  {
    this.capitalRecibido = cR;
  }
  public double getCapitalRecibido()
  {
    return capitalRecibido;
  }
  public void setImportancia(double i)
  {
    this.importancia = i;
  }
  public double getImportancia()
  {
    return importancia;
  }
}